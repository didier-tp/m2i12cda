
package com.mycompany.app2;


public class Stats {
    private int nbPers;
    private int tailleMax;
    private int tailleMini;
    private double tailleMoyenne;

    public Stats() {
    }
    
    

    public int getNbPers() {
        return nbPers;
    }

    public void setNbPers(int nbPers) {
        this.nbPers = nbPers;
    }

    public int getTailleMax() {
        return tailleMax;
    }

    public void setTailleMax(int tailleMax) {
        this.tailleMax = tailleMax;
    }

    public int getTailleMini() {
        return tailleMini;
    }

    public void setTailleMini(int tailleMini) {
        this.tailleMini = tailleMini;
    }

    public double getTailleMoyenne() {
        return tailleMoyenne;
    }

    public void setTailleMoyenne(double tailleMoyenne) {
        this.tailleMoyenne = tailleMoyenne;
    }
    
    
}
